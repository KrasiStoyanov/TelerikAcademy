namespace DebuggingPractise
{
    using System;

    public class DebuggingPractise
    {
        public static void Main()
        {
            const int START_NUMBER = 1;
            const int END_NUMBER = 1000;
            for (int i = START_NUMBER; i <= END_NUMBER; i++)
            {
                Console.WriteLine(i);
            }

            Console.WriteLine();
        }
    }
}
